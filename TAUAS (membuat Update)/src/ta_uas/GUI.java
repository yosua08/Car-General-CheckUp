    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int dialogButton = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Anda yakin ingin menghapus data ini?", "Delete", dialogButton);
        if(dialogResult == 0) {
            dtm.removeRow(row);
            nKendaraan.remove(row);
            dtm.setRowCount(1);
            for (int i = 0; i < nKendaraan.size(); i++){
                Object[] objs = {nKendaraan.get(i).kode, nKendaraan.get(i).nama, nKendaraan.get(i).alamat, nKendaraan.get(i).mobil, nKendaraan.get(i).roda, nKendaraan.get(i).jbakar, nKendaraan.get(i).part, nKendaraan.get(i).kerusakan, nKendaraan.get(i).kategori(nKendaraan.get(i).kerusakan)};
                dtm.addRow(objs);
            }
        } else {
        
        }
    }//GEN-LAST:event_deleteActionPerformed

    private void btn_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_updateActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        if(jTable1.getSelectedRow () ==-1){
            if(jTable1.getRowCount() ==0) {
                JOptionPane.showMessageDialog(null, "Tidak ada data Konsumen di dalam tabel");
            }else{
                JOptionPane.showMessageDialog(null, "Pilih data Konsumen yang akan diubah");
            }
        }else{
            model.setValueAt(txt_kode.getText(), jTable1.getSelectedRow(), 0);
            model.setValueAt(txt_nama.getText(), jTable1.getSelectedRow(), 1);
            model.setValueAt(txt_alamat.getText(), jTable1.getSelectedRow(), 2);
            model.setValueAt(txt_mobil.getText(), jTable1.getSelectedRow(), 3);
            model.setValueAt(txt_roda.getText(), jTable1.getSelectedRow(), 4);
            model.setValueAt(txt_bahanBakar.getText(), jTable1.getSelectedRow(), 5);
            model.setValueAt(txt_bagianCheck.getText(), jTable1.getSelectedRow(), 6);
            model.setValueAt(txt_kerusakan.getText(), jTable1.getSelectedRow(), 7);           
            JOptionPane.showMessageDialog(null, "Data Konsumen Berhasil di Update");
        }
        reset();
        txt_kode.setText("");
        txt_nama.setText("");
        txt_alamat.setText("");
        txt_mobil.setText("");
        txt_roda.setText("");
        txt_bahanBakar.setText("");
        txt_bagianCheck.setText("");
        txt_kerusakan.setText("");
    }//GEN-LAST:event_btn_updateActionPerformed

