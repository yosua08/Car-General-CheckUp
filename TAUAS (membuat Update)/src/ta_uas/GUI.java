    private void btn_inputActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_inputActionPerformed
        // TODO add your handling code here:
        int kode = Integer.parseInt(txt_kode.getText());
        String nama = txt_nama.getText();
        String alamat = txt_alamat.getText();
        String mobil = txt_mobil.getText();
        int roda = Integer.parseInt(txt_roda.getText());
        String jbakar = txt_bahanBakar.getText();
        String part = txt_bagianCheck.getText();
        int kerusakan = Integer.parseInt(txt_kerusakan.getText());
        
        nKendaraan.add(new Kendaraan(kode, nama, alamat, mobil, roda, jbakar, part, kerusakan));
        
        dtm.setRowCount(0);
        for (int i = 0; i < nKendaraan.size(); i++){
            Object[] objs = {nKendaraan.get(i).kode, nKendaraan.get(i).nama, nKendaraan.get(i).alamat, nKendaraan.get(i).mobil, nKendaraan.get(i).roda, nKendaraan.get(i).jbakar, nKendaraan.get(i).part, nKendaraan.get(i).kerusakan, nKendaraan.get(i).kategori(kerusakan)};
            dtm.addRow(objs);
        }
        
        txt_kode.setText("");
        txt_nama.setText("");
        txt_alamat.setText("");
        txt_mobil.setText("");
        txt_roda.setText("");
        txt_bahanBakar.setText("");
        txt_bagianCheck.setText("");
        txt_kerusakan.setText("");
    }//GEN-LAST:event_btn_inputActionPerformed

